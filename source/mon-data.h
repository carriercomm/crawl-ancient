/* ******************************************************************

   (see "mon-util.h" for the gory details)

 - ordering does not matter, because seekmonster() searches the entire
   array ... probably not to most efficient thing to do, but so it goes

 - Here are the rows:
    - row 1: monster id, display character, display colour, name
    - row 2: monster flags
    - row 3: monster resistance flags
    - row 4: mass, xp mod, genus, species, holiness, resist magic
    - row 5: up to four attacks { type (verb), flavour (side effect), damage }
    - row 6: hit dice { HD, min, random extra, plus }
    - row 7: AC, EV, speed, attack cost, spells, corpse flavour, zombie size,
             shouts, intel
    - row 8: gmon_use class, size

 - Some further explanations:
    - xp mod: used to scale (klude) XP calculation (but only when truly needed)
    - mass: if zero, the monster never leaves a corpse
    - genus: marks "teams", mimics, imps, undead hierarchy
    - species: base monster "type" for a classed monsters

    - holiness: holy     - irritates some gods when killed, immunity from
                           holy wrath weapons

                natural  - baseline monster type

                undead   - immunity from draining, pain, torment; extra
                           damage from hoyl wrath/disruption; affected by
                           repel undead and holy word

                demonic   - similar to undead, but holy wrath does even
                            more damage and disruption and repel undead
                            effects are ignored -- *no* automatic hellfire
                            resistance

                nonliving - constructs, immune to things that require
                            being alive, essentially neutral

                plant     - living, immune to mental effects, neutral

    - hit dice (note that min + rand is now loosly based on size):
        - hit points = HD * [ min, min + rand ] + plus
            - min = HD * min + plus
            - max = HD * (min + rand) + plus
            - avg = HD * (2 * min + rand) / 2 + plus

    - speed: equals the amount of energy the monster gets per 10 time ticks

    - attack_cost:
        - this (* wpn speed) is subtracted from energy for melee attacks
        - eg speed 10, attack_cost 5 for a weapon wielder is like top PC skill
        - (speed/attack_cost) is a measure of attacks per round (unless the
          monster wields a weapon, in which case you need to consider it's
          speed as well).

 */


